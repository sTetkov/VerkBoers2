    private void loadOwnArticles() {
	ArticleListMessageRequest req = new ArticleListMessageRequest(
		user.getId(), showOffers);
	ccm.addMessage(req, this);
	dialog = new JDialog();
	dialog.setTitle("Waiting for Login");
	dialogLabel = new JLabel();
	dialogLabel.setText("Waiting to send Message...");
	dialog.add(dialogLabel);
	dialog.setModal(true);
	dialog.show();
    }

    private void loadOffers() {
	ArticleListMessageRequest req = new ArticleListMessageRequest(
		user.getId(), !showOffers);
	ccm.addMessage(req, this);
	dialog = new JDialog();
	dialog.setTitle("Waiting for Login");
	dialogLabel = new JLabel();
	dialogLabel.setText("Waiting to send Message...");
	dialog.add(dialogLabel);
	dialog.setModal(true);
	dialog.show();
    }

