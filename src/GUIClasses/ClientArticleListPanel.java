    @Override
    public void MessageSent() {
	dialogLabel.setText("Message Sent, Waiting for answer");
	dialog.invalidate();
    }

    @Override
    public void AnswerReceived(IVBMessage answer) {
	dialog.hide();
	switch (answer.MsgType()) {
	case ArticleListMessageAnswer:
	    ArticleListAnswerPayload payload = (ArticleListAnswerPayload) answer
		    .getPayload();
	    artVec = payload.getList();
	    loadList();
	    invalidate();
	    break;
	default:
	    break;
	}

    }

    private void loadList() {
	listComponent.setListData(artVec);
	invalidate();
    }

    @Override
    public void CommunicationError(IVBMessage answer) {
	JOptionPane.showMessageDialog(parent, "ERROR: "
		+ ((OperationFailedAnswer) answer).getReasons());
    }
